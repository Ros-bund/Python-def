def draw_box():
    # Цикл проходит 14 раз (количество строк)
    for i in range(14):
        # Если это первая или последняя строка, печатаем 10 звёздочек
        if i == 0 or i == 13:
            print('*' * 10)
        # В остальных случаях печатаем звезду, 8 пробелов и снова звезду
        else:
            print('*' + ' ' * 8 + '*')

# Вызываем функцию
draw_box()